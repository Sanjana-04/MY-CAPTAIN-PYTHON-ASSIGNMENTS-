#Program to find the area of the circle of given radius
#Let r represent the radius of the circle
#Let A represent the area of the circle
#Let pi be the variable to store the value of pi
import math
r=float(input("Enter the radius of the circle:"))
pi=math.pi
A=pi*r*r
print("The area of the circle is:",A)
